# 1.Arithmetic Operators
""" +   --->    Addition
    -   --->    Subtraction
    *   --->    Multiplication
    /   --->    Division
    %   --->    Modulus
    **  --->    Exponential
    //  --->    Floor Division """

a=5
b=2

print(a+b)          #7
print(a-b)          #3
print(a*b)          #10
print(a**b)         #25
print(a%b)          #1
print(a/b)          #2.5
print(a//b)         #2--> divides the values and gives only the interger value and omits the decimal value

